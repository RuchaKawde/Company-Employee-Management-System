package mypack;

public class Empdata {

	int id;
	String name;
    double salary;
    public void setId(int id) {
		this.id = id;
	}
    public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getSalary() {
		return salary;
	}
	public void setSalary(double salary) {
		this.salary = salary;
	}
	
	

}
